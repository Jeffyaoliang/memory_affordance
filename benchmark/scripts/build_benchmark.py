"""
Build the final Memory Affordance benchmark from QA generation results + masks.

This script:
1. Reads QA pairs generated by episode_qa_generation.py
2. Runs Rex-Omni + SAM2 to generate ground-truth masks for each target object
3. Packages everything into the final benchmark format

Final benchmark format (per episode):
{
    "episode_id": "P01_01",
    "images": [
        {"image_id": 0, "filename": "frame_00000000.jpg", "frame_idx": 0, "timestamp_sec": 0.0},
        ...
    ],
    "tasks": [
        {
            "task_id": "P01_01_t0",
            "instruction": "I need to slice these vegetables...",
            "target_image_id": 7,
            "target_object": "knife on the cutting board",
            "mask_path": "P01_01/masks/t0_mask.png",
            "difficulty": "easy"  # easy/medium/hard
        },
        ...
    ]
}

Usage:
    python build_benchmark.py \
        --qa_results_dir results/episode_qa \
        --keyframes_dir data/keyframes \
        --output_dir benchmark/final \
        --run_segmentation  # optional: generate masks with Rex-Omni + SAM2
"""

import argparse
import json
import os
import shutil
from pathlib import Path
from tqdm import tqdm


def load_qa_results(qa_dir: str) -> list[dict]:
    """Load all QA generation results."""
    results = []
    for ep_name in sorted(os.listdir(qa_dir)):
        qa_path = os.path.join(qa_dir, ep_name, "qa_pairs.json")
        if os.path.exists(qa_path):
            with open(qa_path) as f:
                results.append(json.load(f))
    return results


def build_episode_benchmark(qa_result: dict, keyframes_dir: str,
                            output_dir: str, run_seg: bool = False) -> dict:
    """Build benchmark entry for a single episode."""
    episode_name = qa_result["episode_name"]
    ep_output = os.path.join(output_dir, episode_name)
    os.makedirs(ep_output, exist_ok=True)
    os.makedirs(os.path.join(ep_output, "images"), exist_ok=True)
    os.makedirs(os.path.join(ep_output, "masks"), exist_ok=True)

    # Copy keyframe images
    kf_dir = os.path.join(keyframes_dir, episode_name)
    images_meta = []
    for img_info in qa_result["images"]:
        src = os.path.join(kf_dir, img_info["filename"])
        dst = os.path.join(ep_output, "images", img_info["filename"])
        if os.path.exists(src) and not os.path.exists(dst):
            shutil.copy2(src, dst)
        images_meta.append({
            "image_id": img_info["image_id"],
            "filename": img_info["filename"],
            "frame_idx": img_info.get("frame_idx", img_info["image_id"]),
            "timestamp_sec": img_info.get("timestamp_sec", 0),
        })

    # Build tasks
    tasks = []
    for i, qa in enumerate(qa_result["qa_pairs"]):
        task_id = f"{episode_name}_t{i}"
        task = {
            "task_id": task_id,
            "instruction": qa["task_instruction"],
            "target_image_id": qa["image_id"],
            "target_object": qa["object"],
            "mask_path": f"{episode_name}/masks/{task_id}_mask.png",
            "difficulty": "unknown",  # to be annotated later or auto-classified
        }

        # Generate mask if requested
        if run_seg:
            target_img_info = next(
                (img for img in qa_result["images"] if img["image_id"] == qa["image_id"]),
                None
            )
            if target_img_info:
                img_path = os.path.join(ep_output, "images", target_img_info["filename"])
                mask_path = os.path.join(ep_output, "masks", f"{task_id}_mask.png")
                if os.path.exists(img_path) and not os.path.exists(mask_path):
                    # Placeholder: actual segmentation would be done by label-sam2.py
                    task["mask_generated"] = False
                    task["mask_note"] = "Run label-sam2.py separately to generate masks"

        tasks.append(task)

    benchmark_entry = {
        "episode_id": episode_name,
        "num_images": len(images_meta),
        "num_tasks": len(tasks),
        "images": images_meta,
        "tasks": tasks,
    }

    # Save per-episode benchmark file
    with open(os.path.join(ep_output, "benchmark.json"), "w") as f:
        json.dump(benchmark_entry, f, indent=2, ensure_ascii=False)

    return benchmark_entry


def main():
    parser = argparse.ArgumentParser(description="Build Memory Affordance benchmark")
    parser.add_argument("--qa_results_dir", type=str, required=True,
                        help="Directory with QA generation results")
    parser.add_argument("--keyframes_dir", type=str, required=True,
                        help="Directory with extracted keyframes")
    parser.add_argument("--output_dir", type=str, default="benchmark/final")
    parser.add_argument("--run_segmentation", action="store_true",
                        help="Generate masks (requires Rex-Omni + SAM2)")
    args = parser.parse_args()

    # Load QA results
    qa_results = load_qa_results(args.qa_results_dir)
    print(f"Loaded {len(qa_results)} episodes with QA results")

    # Build benchmark
    all_entries = []
    total_tasks = 0
    for qa_result in tqdm(qa_results, desc="Building benchmark"):
        entry = build_episode_benchmark(
            qa_result, args.keyframes_dir, args.output_dir, args.run_segmentation
        )
        all_entries.append(entry)
        total_tasks += entry["num_tasks"]

    # Save master benchmark file
    master = {
        "benchmark_name": "Memory Affordance Benchmark",
        "version": "0.1",
        "num_episodes": len(all_entries),
        "total_tasks": total_tasks,
        "episodes": [
            {
                "episode_id": e["episode_id"],
                "num_images": e["num_images"],
                "num_tasks": e["num_tasks"],
            }
            for e in all_entries
        ],
    }
    master_path = os.path.join(args.output_dir, "benchmark_master.json")
    with open(master_path, "w") as f:
        json.dump(master, f, indent=2, ensure_ascii=False)

    print(f"\nBenchmark built: {len(all_entries)} episodes, {total_tasks} tasks")
    print(f"Master file: {master_path}")


if __name__ == "__main__":
    main()
