"""
Episode-level QA uniqueness check.

For each QA pair generated by episode_qa_generation.py, this script feeds the
ENTIRE episode (all images) along with the QA pair to a VLM and asks it to
verify that the target object is the ONLY object across ALL frames that can
satisfy the task_instruction.

Optionally supports running with multiple VLMs and taking the intersection of
their valid sets (paranoid mode).

Usage:
    # Single-model check
    python qa_uniqueness_check.py \
        --qa_results_dir results/episode_qa \
        --model_name qwen3.5-397b-a17b \
        --api_url https://dashscope.aliyuncs.com/compatible-mode/v1 \
        --api_key sk-xxx

    # Multi-model intersection (paranoid)
    python qa_uniqueness_check.py \
        --qa_results_dir results/episode_qa \
        --multi_model qwen3.5-397b-a17b,gemini-2.5-flash \
        --intersect
"""

import argparse
import json
import os
import re
import sys
from pathlib import Path
from PIL import Image
from tqdm import tqdm

ANNOTATOR_DIR = os.path.join(os.path.dirname(__file__), "..", "..", "Affordance_Annotator")
sys.path.insert(0, ANNOTATOR_DIR)
from utils.model_utils import create_model, Prompt

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
DEFAULT_PROMPT_PATH = os.path.join(SCRIPT_DIR, "..", "prompts", "episode_qa_check.md")


def load_episode_images(qa_result: dict, keyframes_root: str) -> list[dict]:
    """Load PIL images for an episode based on QA result metadata.

    Looks up images via qa_result["images"] (filename + image_id) and tries to
    find them in qa_result["episode_dir"] first, then keyframes_root.
    """
    images = []
    episode_dir = qa_result.get("episode_dir")
    episode_name = qa_result["episode_name"]

    candidate_dirs = []
    if episode_dir and os.path.isdir(episode_dir):
        candidate_dirs.append(episode_dir)
    if keyframes_root:
        candidate_dirs.append(os.path.join(keyframes_root, episode_name))

    for img_info in qa_result["images"]:
        fname = img_info["filename"]
        img_path = None
        for d in candidate_dirs:
            p = os.path.join(d, fname)
            if os.path.exists(p):
                img_path = p
                break
        if img_path is None:
            continue
        img = Image.open(img_path)
        if img.mode == "RGBA":
            img = img.convert("RGB")
        images.append({
            "image_id": img_info["image_id"],
            "image": img,
            "filename": fname,
        })
    return images


def build_check_prompt(system_prompt: str, episode_images: list[dict],
                       qa_pair: dict) -> tuple[str, list[Image.Image]]:
    """Build interleaved text+image prompt for one QA check.

    Format:
        <system_prompt>

        ### Episode Images
        [Image 0] <img> [Image 1] <img> ...

        ### QA Pair to Evaluate
        target_image_id: 5
        target_object: "..."
        task_instruction: "..."

        ### Decision
    """
    parts = [system_prompt, "\n\n### Episode Images\n\n"]
    pil_images = []
    for item in episode_images:
        parts.append(f"[Image {item['image_id']}] ")
        pil_images.append(item["image"])

    parts.append("\n\n### QA Pair to Evaluate\n")
    parts.append(json.dumps({
        "target_image_id": qa_pair["image_id"],
        "target_object": qa_pair["object"],
        "task_instruction": qa_pair["task_instruction"],
    }, ensure_ascii=False))
    parts.append("\n\n### Decision\nReturn a single JSON object as specified.")

    return "".join(parts), pil_images


def parse_check_response(response_text: str) -> dict:
    """Extract the {is_valid, step_failed, reason} JSON from VLM output."""
    text = re.sub(r'^```(json)?', '', response_text, flags=re.MULTILINE)
    text = re.sub(r'```$', '', text, flags=re.MULTILINE)
    decoder = json.JSONDecoder()
    text = text.strip()
    # Find the first { and try to decode
    start = text.find('{')
    if start < 0:
        return {"is_valid": False, "step_failed": -1,
                "reason": "no JSON found in response", "raw": response_text[:200]}
    try:
        obj, _ = decoder.raw_decode(text[start:])
        # Normalize fields
        return {
            "is_valid": bool(obj.get("is_valid", False)),
            "step_failed": int(obj.get("step_failed", -1)),
            "reason": str(obj.get("reason", "")),
        }
    except json.JSONDecodeError as e:
        return {"is_valid": False, "step_failed": -1,
                "reason": f"JSON parse error: {e}", "raw": response_text[:200]}


def check_episode(qa_result: dict, keyframes_root: str, models: list,
                  system_prompt: str, intersect: bool, output_dir: str):
    """Run uniqueness check on all QA pairs of one episode.

    Args:
        models: list of (name, BaseModelInterface). If multiple, optionally
                intersect their valid verdicts.
    """
    episode_name = qa_result["episode_name"]
    save_dir = os.path.join(output_dir, episode_name)
    os.makedirs(save_dir, exist_ok=True)

    # Skip if already done
    out_path = os.path.join(save_dir, "qa_check_results.json")
    if os.path.exists(out_path):
        with open(out_path) as f:
            return json.load(f)

    # Load episode images once
    episode_images = load_episode_images(qa_result, keyframes_root)
    if not episode_images:
        print(f"  [{episode_name}] no images found, skipping")
        return None

    qa_pairs = qa_result["qa_pairs"]
    if not qa_pairs:
        return None

    print(f"  [{episode_name}] {len(qa_pairs)} QA pairs × {len(models)} model(s) × {len(episode_images)} images")

    # Per-model results: {model_name: [check_dict_per_qa]}
    per_model_results = {name: [] for name, _ in models}

    for qa in qa_pairs:
        prompt_text, pil_images = build_check_prompt(system_prompt, episode_images, qa)
        for name, model in models:
            try:
                resp = model.generate(Prompt(text=prompt_text, images=pil_images))
                check = parse_check_response(resp.text)
            except Exception as e:
                check = {"is_valid": False, "step_failed": -1,
                         "reason": f"API error: {e}"}
            check["qa"] = qa
            per_model_results[name].append(check)

    # Compute valid set
    if intersect and len(models) > 1:
        # Valid only if ALL models say valid
        valid_qas = []
        n = len(qa_pairs)
        for i in range(n):
            if all(per_model_results[name][i]["is_valid"] for name, _ in models):
                valid_qas.append(qa_pairs[i])
    else:
        # Use first model
        first_name = models[0][0]
        valid_qas = [r["qa"] for r in per_model_results[first_name] if r["is_valid"]]

    result = {
        "episode_name": episode_name,
        "num_qa_total": len(qa_pairs),
        "num_qa_valid": len(valid_qas),
        "intersect_mode": intersect,
        "models": [name for name, _ in models],
        "per_model_results": per_model_results,
        "valid_qa_pairs": valid_qas,
    }

    with open(out_path, "w") as f:
        json.dump(result, f, indent=2, ensure_ascii=False)
    with open(os.path.join(save_dir, "valid_qa_pairs.json"), "w") as f:
        json.dump(valid_qas, f, indent=2, ensure_ascii=False)

    print(f"  [{episode_name}] valid: {len(valid_qas)}/{len(qa_pairs)}")
    return result


def main():
    parser = argparse.ArgumentParser(description="Episode-level QA uniqueness check")
    parser.add_argument("--qa_results_dir", type=str, required=True,
                        help="Directory with episode_qa_generation.py output")
    parser.add_argument("--keyframes_root", type=str, default=None,
                        help="Fallback root for keyframe images if episode_dir not found")
    parser.add_argument("--output_dir", type=str, default=None,
                        help="Defaults to qa_results_dir (writes alongside)")
    parser.add_argument("--prompt_path", type=str, default=DEFAULT_PROMPT_PATH)
    parser.add_argument("--model_name", type=str, default="qwen3.5-397b-a17b")
    parser.add_argument("--multi_model", type=str, default=None,
                        help="Comma-separated model names for multi-model check")
    parser.add_argument("--intersect", action="store_true",
                        help="Require ALL models to mark valid (paranoid mode)")
    parser.add_argument("--api_url", type=str,
                        default="https://dashscope.aliyuncs.com/compatible-mode/v1")
    parser.add_argument("--api_key", type=str, default=None)
    parser.add_argument("--max_tokens", type=int, default=1024)
    parser.add_argument("--temperature", type=float, default=0.0)
    args = parser.parse_args()

    if args.api_key is None:
        args.api_key = os.environ.get("QWEN_AUTH_TOKEN", "")
    if args.output_dir is None:
        args.output_dir = args.qa_results_dir

    with open(args.prompt_path) as f:
        system_prompt = f.read()

    # Build model list
    if args.multi_model:
        names = [n.strip() for n in args.multi_model.split(",") if n.strip()]
    else:
        names = [args.model_name]

    models = []
    for name in names:
        m = create_model(
            model_name=name,
            api_url=args.api_url,
            api_key=args.api_key,
            max_tokens=args.max_tokens,
            temperature=args.temperature,
        )
        models.append((name, m))

    print(f"Using {len(models)} model(s): {[n for n, _ in models]}")
    if args.intersect and len(models) > 1:
        print("Intersection mode: QA must be valid in ALL models")

    # Load all QA results
    episodes = []
    for ep_name in sorted(os.listdir(args.qa_results_dir)):
        qa_path = os.path.join(args.qa_results_dir, ep_name, "qa_pairs.json")
        if os.path.exists(qa_path):
            with open(qa_path) as f:
                episodes.append(json.load(f))

    print(f"Found {len(episodes)} episodes with QA pairs")

    summary = []
    for ep in tqdm(episodes, desc="Episodes"):
        result = check_episode(ep, args.keyframes_root, models, system_prompt,
                               args.intersect, args.output_dir)
        if result:
            summary.append({
                "episode_name": result["episode_name"],
                "num_qa_total": result["num_qa_total"],
                "num_qa_valid": result["num_qa_valid"],
            })

    total = sum(s["num_qa_total"] for s in summary)
    valid = sum(s["num_qa_valid"] for s in summary)
    print(f"\nDone. {len(summary)} episodes processed.")
    print(f"Total QA: {total}, Valid: {valid} ({100*valid/total if total else 0:.1f}%)")

    summary_path = os.path.join(args.output_dir, "qa_check_summary.json")
    with open(summary_path, "w") as f:
        json.dump({
            "models": names,
            "intersect": args.intersect,
            "total_qa": total,
            "valid_qa": valid,
            "episodes": summary,
        }, f, indent=2)
    print(f"Summary saved to {summary_path}")


if __name__ == "__main__":
    main()
